"use strict";
/**
 * Array datatype
 */
let myArr = ["Agus", "Asep", "Iqbal"]; // array with string value
// myArr = [1, 2, 3]; // error, because the array is not string value
let arrayNumbers = [1, 2, 3]; // array with number value
let arrayAny = [1, "Asep", true]; // array with any value
console.log(myArr);
console.log(arrayNumbers);
console.log(arrayAny);
/**
 * Tuple datatype
 */
let biodata;
biodata = ["Agus", 23, false];
console.log(biodata);
